    private ArrayList<Enterprise> enterpriseList() {
        ArrayList<Enterprise> enterpriseList = new ArrayList<>();
        
        Database db = new Database();
        
        try {
            conn = db.connect();
            String sql = "select * from enterprises";
            PreparedStatement statement = conn.prepareStatement(sql);
            ResultSet a = statement.executeQuery();
            Enterprise e;
            while(a.next()) {
                enterpriseList.add(new Enterprise(a.getInt("id"),a.getString("enterprise"),a.getString("network")));
            }
        } catch (Exception ex) {
            Logger.getLogger(ViewNetworkJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return enterpriseList;
    }
    private void addEnterpriseData(){
        ArrayList<Enterprise> enterpriseList = enterpriseList();
        DefaultTableModel model = (DefaultTableModel) enterpriseData.getModel();
        model.setRowCount(0);
        for(Enterprise e: enterpriseList()){
            Object[] row = new Object[3];
            
            row[0] = e.getEnterpriseId();
            row[1] = e.getEnterpriseName();
            row[2] = e.getNetworkName();
            
            model.addRow(row);
        }
    }

