    private ArrayList<Organisation> organisationList() {
        ArrayList<Organisation> organisationList = new ArrayList<>();
        
        Database db = new Database();
        
        try {
            conn = db.connect();
            String sql = "select * from organisations";
            PreparedStatement statement = conn.prepareStatement(sql);
            ResultSet a = statement.executeQuery();
            Organisation o;
            while(a.next()) {
                organisationList.add(new Organisation(a.getInt("id"),a.getString("organisation"),a.getString("enterprise"),a.getString("network")));
            }
        } catch (Exception ex) {
            Logger.getLogger(ViewOrganisationJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return organisationList;
    }
    private void addOrganisationsData(){
        ArrayList<Organisation> organisationList = organisationList();
        DefaultTableModel model = (DefaultTableModel) organisationData.getModel();
        model.setRowCount(0);
        for(Organisation o: organisationList()){
            Object[] row = new Object[4];
            
            row[0] = o.getOrganisationId();
            row[1] = o.getOrganisationName();
            row[2] = o.getEnterpriseName();
            row[3] = o.getNetworkName();
            
            model.addRow(row);
        }
    }

