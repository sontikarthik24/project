    private void policeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_policeButtonActionPerformed
        // TODO add your handling code here:
        int rowIndex = passportData.getSelectedRow();
        if (rowIndex<0){
            JOptionPane.showMessageDialog(this, "Please select a row for sending it to police", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        Database db = new Database();
        
        try {
            Connection conn = db.connect();
            String sql = "update passportdata set status=? where fileno=?";
            PreparedStatement statement = conn.prepareStatement(sql);
            statement.setString(1, "Sent to police");
            statement.setString(2, passportData.getValueAt(rowIndex, 0).toString());
            statement.executeUpdate();
            JOptionPane.showMessageDialog(this, "Successfully Assigned to Police for Verification", "Success", JOptionPane.INFORMATION_MESSAGE);
            addPassportData();
                    
        } catch (Exception ex) {
            Logger.getLogger(PasportAdminJPanel.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_policeButtonActionPerformed


